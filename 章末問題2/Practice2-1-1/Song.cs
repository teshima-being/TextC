namespace Practice2_1_1 {

    /// <summary>
    /// 以下のプロパティを持つSongクラス
    /// Title：string型(歌のタイトル)
    /// ArtistName：string型(アーティスト名)
    /// Length：int型(演奏時間)
    /// </summary>
    internal class Song {

        /// <summary>
        /// /歌のタイトル
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// アーティスト名
        /// </summary>
        public string ArtistName { get; set; }

        /// <summary>
        /// 演奏時間
        /// </summary>
        public int Length { get; set; }
    }
}
